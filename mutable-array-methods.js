//1.- pop
let one = [1,2,3];

one.pop();

console.log(one);

//2.- push

one.push(3);

console.log(one);

//3.-shift removes the first element of an array

one.shift();

console.log(one);

//4.-unshift adds elements at the beginning of an array 

one.unshift(1);

console.log(one);

//5.-Reverse 

one.reverse();

console.log(one);

//6.-Sort

one.sort();

console.log(one);

//7.-Splice removes or replaces elements in array

one.splice(4, 1, 5);

console.log(one);

